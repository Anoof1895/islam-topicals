// Function to get topic name with fallback
export const getTopicName = (book, unit, topic) => {
  const topicStr = String(topic);
  const topics = topicStr.split('_');
  
  const topicNamesArray = topics.map(t => {
    return topicNames[book]?.[unit]?.[t] || `Topic ${t}`;
  });
  
  return topicNamesArray.join(', ');
};